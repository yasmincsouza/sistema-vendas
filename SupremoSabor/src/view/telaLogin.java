    public void consultarUsuario(){ 
      usuario = txtUsuario.getText();
      senha = txtSenha.getText();
      
        Connection con = connectionFactory.obterConexao();
        PreparedStatement stmt = null;
        ResultSet rs = null ;
        
    try {
        stmt = (PreparedStatement) con.prepareStatement("SELECT * FROM usuario WHERE Login LIKE ? and Senha LIKE ?");
        stmt.setString(1, usuario);
        stmt.setString(2, senha);
        rs = stmt.executeQuery();
        if (rs.next()){
            
            JOptionPane.showMessageDialog(null, "Acesso liberado");
            telaMenu TM = new telaMenu();
            TM.setVisible(true);
            TM.lblLogin.setText("Olá, " + txtUsuario.getText());
            this.dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Acesso negado");
        }
        Limpar();     
          
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Problemas com a execução" + ex);
    } finally{
        connectionFactory.fecharConexao(con, stmt, rs);
    }
    }

